# El programa, dado como dato la calificación de un alumno en un examen, escribe "aprobado" si su calificación es mayor o igual que 8 y "reprobado" en caso contrario
CAL = float (input("Ingresa tu calificacion de examen: "))
if CAL >= 8:
    print (f" Aprobrado")
else: 
    print (f" Reprobado")
print (" Fin del programa ")    
